package model

import (
	_ "github.com/go-sql-driver/mysql"
	"github.com/jmoiron/sqlx"
	"github.com/spf13/viper"
)

func NewDb() (*sqlx.DB, error) {
	db, err := sqlx.Open(viper.GetString("driver"), viper.GetString("dsn"))
	if err != nil {
		return nil, err
	}

	err = db.Ping()
	if err != nil {
		return nil, err
	}

	return db, nil
}
